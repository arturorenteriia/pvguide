<p class="text-justify"> 
Tipping is generally similar to how you would tip in the U.S or Canada. percentage wise, 10% to 15% is generally an acceptable amount on the bill.
Easiest way for a tourist to remember is to tip a maximum 15 pesos for every 100 pesos you spend on a meal with good service, If you feel you were short changed and given poor customer service, then 10 pesos on every 100 spent. 

If your tipping in US dollars, then just remember the average exchange rate is around 18 pesos to the $ 1  US dollar. Tipping a U.S dollar for  every 100 pesos is generous and more than acceptable.

