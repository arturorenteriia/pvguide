Tipping Hotel / Bar
Bartender - 
Could be 10 to 20% of the total drink bill. ( $ 1 dollar on every 100 pesos spent or 20 pesos on every hundred spent is generous ) and easier than trying to work out percentages !

Delivery Person - Could be tipped between 20 to 50 pesos or $ 2 to  $ 3 US dollars minimum.

</p> <p class="text-justify">Restaurants/ Bars/Clubs  - Tipping is generally 15-20% of the bill ( $ 1 U.S dollar on every  100 pesos spent or 20 pesos on every 100 pesos spent )

</p> <p class="text-justify">Baggage Handling - 20 to 50 Pesos (about $1  to $ 4 USD/CAD). 
A bus driver could be tipped $10 to $20 Pesos if luggage is handled ( $ 1 to 2 U.S dollars ) a chauffeur, between 50 to a 100 pesos ( $ 3 to $ 5 U.S dollars ) a porter could be tipped 50 pesos.
