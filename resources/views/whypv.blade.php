                  <h1><center>9 Reasons to head to Puerto Vallarta, Mexico’s beachy resort. </center>
               </h1>
               <br>
              <DIV >
              	<p class="text-justify">
Mexico is a perennial favorite for American travelers looking for a beach getaway. Yet not all beach destinations were created equal. Some feel like they were built yesterday merely as a place to disgorge jumbo jets. On the other hand, long before tourists discovered it, Puerto Vallarta was a charming town of white stucco, terra-cotta tile roofs and cobblestone streets clinging to the lush green hillsides spilling into the Pacific waters of Banderas Bay. And it still is.
<br>
In addition to sun-kissed beaches and aquamarine waters, Puerto Vallarta offers great gastronomy, rich culture and an enchanting town.

</p>

  <br>
<h4><b>Hip History</b></h4>
<br>
<p class="text-justify">
Ever since the 1950s, artists and other creative types have been drawn to the spectacular scenery and laid back charm. John Huston shot Night of the Iguana with Richard Burton nearby in 1963. When Elizabeth Taylor came to keep an eye on her lover, hundreds of reporters followed, putting Puerto Vallarta on the map. Liz and Dick fell in love with the place and bought adjoining houses, which you can see in Gringo Gulch. Bob Dylan and other 60s rockers flocked here for the bohemian atmosphere and the, ahem, benefits of having peyote-eating Huichol Indians nearby.</p>
      
<br>

<h4><b>Culture</b></h4>
<br>
<p class="text-justify">
Vallarta has a vibrant art scene that goes way beyond paintings of sunsets. Downtown galleries open for an Art Walk on Wednesday evenings November through May, offering exhibitions, cocktails and a chance to meet the artists, expats, locals and gallery owners, such as Jan Lavender of Galeria Uno. She can regale you with stories from the days when Liz, Dick and John Huston caroused here. <br>
Puerto Vallarta is proud of its pre-Columbian heritage. The Huichol Indians are known for yarn paintings and bead art in mind-blowing colors inspired by the hallucinogenic peyote cactus. Huichol symbols are embedded mosaic-style in Vallarta’s sea front promenade, the malecon. Canadian Kevin Simpson came here over 20 years ago; his galleries, Colektika and Peyote People, have an impressive collection of art and artifacts by the Huichol and other tribes. The intrepid traveler can jump in a Cessna with Kevin and visit a Huichol village in the Sierra Madre.</p>
  <br>
<h4><b>Eat</b></h4>
<br>
<p class="text-justify">
It’s fitting that Puerto Vallarta welcomes chefs from around the world to its International Gourmet Festival every November. Year round, you’ll find amazing food of every cuisine, from taco stands to fine dining.</p>
<p class="text-justify">
Ocho Tostadas has been packing them in for lunch at its humble outpost in the Marina with mouth watering smoked marlin tostadas, shrimp aguachile and tuna sashimi that Hiro would dream of.</p>
<p class="text-justify">
La Leche, a gourmet restaurant in the hotel zone, is decorated with 2,800 white milk cans stacked on white metal shelves against white walls. Their signature duck, marinated eight hours then baked for two more with an orange demi-glasse, was easily the best I’ve had. Ever. Anywhere.</p>
<p class="text-justify">
Enjoy a technicolor sunset on the beach while enjoying char-grilled octopus, coconut shrimp, fresh tuna, duck enchiladas with almond mole and other delights at La Palapa, a white table cloth restaurant at Playa de los Muertos in the Zona Romantica.</p>
<p class="text-justify">
If you want to be above it all, take a window table at the appropriately named Vista Grill, high on the hillside overlooking the town and the blue Pacific. Savor adobado shrimp, pork belly and a flan casero desert.</p>
<p class="text-justify">
Café des Artistes has been a high church of fine dining since the ‘70s. The Rose lobster tail with black bean puree, pico de gallo, garlic and butter exemplifies how chef Thierry Blouet combines local Mexican ingredients with French influences and techniques. The 19th century villa’s elegant dining rooms and lush courtyard garden complement the food for ultimate romantic effect.</p>
<p class="text-justify">
Portobello offers Italian in the marina overlooking the million-dollar yachts. Tuna carpaccio was a nice prelude to an excellent grilled branzino with olive oil, garlic, capers and tomatoes.</p>
     

  <br>
<h4><b>Shopping</b></h4>
<br>
<p class="text-justify">
Boutiques, jewelry ateliers and galleries make Basilio Badillo Street in the Zona Romantica a mecca for shopping and browsing. Anothers palces to shopping are Galerias Vallarta, Plaza la Isla, Plaza Caracol and Macroplaza </p>
   
<br>
<h4><b>Music</b></h4>
<br>
<p class="text-justify">
Vallarta has more than mariachis. Cuban bands will keep you salsa-ing till the wee hours at La Bodequita del Medio, the local branch of the famed Havana haunt. Across the street, you’ll find cool jazz with musicians from Mexico and abroad at the Jazz Foundation, a loft-style venue with terrace overlooking the sea front. Vallarta native Jorge Dau, who attended high school with Paris Hilton in New York, opened the club with a love for jazz and a desire to bring people. “We’re doing this for Puerto Vallarta, not for tourism,” Dau tells me, sipping mezcal from a brandy glass as Bobby Garcia belts a soulful “Stand By Me” on sax.</p>
   
<br>
<h4><b>LBGT Friendly</b></h4>
<br>
<p class="text-justify">
Puerto Vallarta has been LGBT friendly ever since Liz Taylor showed up. The Zona Romantica is the center of the scene, with bars, hotels and nightclubs catering to LGBT tourists. Even hotels that aren’t geared to the gay and lesbian crowd train staff to be sensitive to the sense and sensibilities of LGBT guests.</p>
  <br>
<h4><b>Get Outta Town</b></h4>
<br>
<p class="text-justify">
Vallarta Adventures offers various day trip excursions on the water and in the Sierra Madre mountains. The maritime national park of Los Arcos is a popular spot for snorkeling and diving. For a more intimate experience, head further on to pocket size beaches accessible only by sea nestle in coves in the jungle-covered hillsides.</p>
<p class="text-justify">
The seaside village of Yelapa, population circa 1,000, is surrounded by pristine jungle and accessible pretty much only from the sea. Water taxis from Vallarta land on the beach in front of the (only) two beach restaurants. Funky/posh guest houses sprout on the waterfront flanking the beach. It’s a short trek to the waterfall where bracing waters promise relief from the midday heat. As we walk through the village, we pass an old man sitting in the shade reading the Holy Bible. A burro makes deliveries. Sorrel horses munch lazily on impossibly green grass in a sun blasted pasture. A knot of school children in pressed uniforms laugh and shout. It’s a picture of Mexico before mass tourism.</p>
  <br>
<h4><b>Hotels</b></h4>
<br>
<p class="text-justify">
When it comes to accommodations, Puerto Vallarta has something for everyone, from all-inclusive, to golf-centric, adults-only, family friendly, LGBT-friendly and even business hotels.</p>
<p class="text-justify">
Villa Premiere Hotel and Spa is a family-owned, adults-only, luxury boutique oceanfront resort with balconied suites and attracts a stylish crowd. Spend the day poolside or on the beach in a palapa with king-sized bed. With chic décor and ideal location, it offers the best of everything - it’s just a ten-minute walk from the malecon and the town’s rich selection of restaurants and nightlife.</p>
<p class="text-justify">
Casa Velas, a country club style, adults-only, all-inclusive luxury resort in the Marina district, is perfect for honeymooners, couples and the golf club set. With links, an expansive pool, gourmet restaurants, a private beach club and lush gardens of palms, banana plants and bougainvillea, it’s a world unto itself for an exclusive pampered getaway.</p>
<p class="text-justify">
Well-being is the watchword at the family-friendly Westin Resort and Spa Puerto Vallarta. Set beachside among 600 palm trees on a former coconut plantation, the resort attracts an international upscale clientele. Guests enjoy tennis courts, two pools, the largest gym in town, a full-service spa and yoga classes with Madonna’s star teacher Wayne Krassner. You can rent New Balance running shoes and use them on hotel-provided jogging maps. Restaurants offer amazing world and Mexican cuisine including the remarkable sea bass miso at Arrecife. </p>
 
<br>
<h4><b>And finally... It’s easy to get to</b></h4>
<br>
<p class="text-justify">
An international airport with many direct flights from the U.S. makes Puerto Vallarta easily accessible. Direct flights from New York clock in under five hours, less time from the West, Midwest and South.</p>
   
