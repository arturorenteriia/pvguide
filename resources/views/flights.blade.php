<h5>What Happened to Cheap Last Minute Fares?
               </h5>
                <p class="text-justify">Consumers who buy at the last minute might keep in mind that anyone who is hoping to fly in 10 days or fewer from now will see higher prices and they will have the option of going 2 or 3 weeks later to save quite a bit of money. This price discrimination allows airlines to sell more expensive seats to those who can't wait, and cheaper tickets to those who can.</p>
<br>
<h5>When To Wait For Airline Ticket Sales?
               </h5>

<p class="text-justify">Fare sales by various airlines usually appear in the middle of a season (summer, for example) trying to fill up seats for the rest of that season. In some cases they'll announce an autumn fare sale in August, but it always tends to be for times of the year when the fewest people travel.
If you are waiting for a fare sale and wondering when it might appear, it's important to consider the airlines' motivation in announcing them. </p> <p class="text-justify"> Let's say they announced an October fare sale in June, with round-trip fares way lower than those offered in summer, this would actually reduce profit for their business for July through September. If someone is considering paying a high fare to fly in August, the airlines are not motivated to show them a much lower fare if they waited. That would lead to empty seats in late summer, which would be very costly for them.
In almost all cases you are best off waiting until 6 weeks or so before your departure date, or 6 months before your travel date but it's also important to track the fares before that, and keep an eye on fare sales.
This same research by the also said that fare sales usually appear on Tuesdays and are over by the end of Thursday, so check fares early in the week and if a price drops, jump on it ! 
They also found the highest fares showed up for those searching on Saturdays and Sundays, so you might be best off just skipping the weekends for fare research anyway.</p>
